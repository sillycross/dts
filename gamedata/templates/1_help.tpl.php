<p><span class="subtitle2">写在前面的话</span></p>
<p>作为一款网页游戏，BR大逃杀也算是有点年头的东西了。<br>
也许你已经是BR原版的老玩家了；也许你虽然没怎么玩过BR大逃杀，但是对类似的网页游戏有所涉猎；当然更可能的情况是你以前并没有接触过这款网页游戏。<br>
无论你是刚刚在上一局游戏胜出的老手，为了打发下一盘游戏开始前的等待时间而点进来看帮助；<br>
还是你是初出茅庐的菜鸟，刚刚莫名其妙地死亡之后决定好好了解一下游戏的进行方式，<br>
本帮助文档都希望使你了解到关于这款BR大逃杀改进版——ACFUN大逃杀——的更多的知识。</p>
<p>以下就是关于这款游戏的基本说明，包括游戏的操作方式、游戏过程、基本的道具资料以及NPC资料。<BR>
部分内容并未在此说明，则是为了保留一点神秘感，增加游戏的趣味性。</p>
<p>注意，以下帮助文档中的所有资料，都是ACFUN大逃杀v.2.91版本的资料，如果版本号更新则可能出现变动。一切数据以游戏为准，本帮助文档仅供参考。<BR>
本帮助参考了CSIEBR的帮助文档，并且感谢所有参与撰写帮助的玩家的协助。<BR>
