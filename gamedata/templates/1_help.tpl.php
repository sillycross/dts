<p><a name="重击和必杀"><span class="subtitle2">13.重击和必杀</span></a></p>
<p>当怒气达到<span class="yellow">30</span>时，攻击时可能发出<span class="yellow">重击</span>，攻击伤害x1.5。<BR>
如果内定称号是<span class="yellow">“超能力者”</span>，则可以自由控制自己的怒气，释放出<span
class="yellow">“必杀技”</span>，此时攻击伤害x2。<BR>
释放方式为在攻击时向对方<span class="yellow">喊话</span> 。<BR>
释放必杀技会消耗<span class="yellow">50</span>点怒气，如果不喊话则绝对不会释放必杀技。<BR>
此外，如果怒气值积累到<span class="yellow">255</span>，则会100%发出重击或者释放必杀技。 <BR>
NPC也会发出重击和必杀技，请随时小心。</p>
