<p><a name="指令说明"><span class="subtitle2">5.指令说明</span></a></p>

<p><span class="yellow">『移动』</span> 消耗15点体力，离开现在的区域而移动到别的区域。<br>
<span class="yellow">『探索』</span> 消耗15点体力，探索现在的区域。<br>
移动和探索都可能遇到敌人、队友、尸体，遇上突发事件，或者发现物品。<br>
<span class="yellow">『道具使用』</span> 根据道具的类型使用或者装备所选的道具。可拥有的道具上限是5个。</p>


<p><span class="lime">“道具”</span>：<br>
<span class="yellow">『道具合成』</span> 将2或3个不同的道具，合成为一个新道具。常见的合成公式请参考下文<br>
<span class="yellow">『道具丢弃』</span>
将包裹里的道具丢掉。丢弃的道具不会消失，将被放置在当前的地区，自己或别人还能再次找到。<br>
<span class="yellow">『整理包裹』</span>
可以将完全相同的道具合并在一起，节约包裹空间。注意，将有毒的食物和无毒的食物合并，会使全部食物带毒。<br>
<span class="yellow">『卸下装备』</span> 将身上的武器或者防具卸下放置到包裹里。</p>


<p><span class="lime">“恢复”</span>：<br>
<span class="yellow">『睡眠』</span> 根据睡眠的时间和体力上限，按一定比例恢复体力。<br>
<span class="yellow">『治疗』</span>
根据睡眠的时间和生命上限，按一定比例恢复生命，但是不会治疗伤口或解除异常状态。伤口治疗在“特殊”功能内。<br>
<span class="yellow">『静养』</span> 同时恢复生命和体力，同时有一定概率解除异常状态，静养时间越长，解除概率越高。只能在精灵中心和对天使用作战本部内进行。</p>




<p><span class="lime">“特殊”</span>：<br>
<span class="yellow">『商店』</span>
在管理后台、光坂高校或者花菱商厦时，可以进入商店购买道具。购买前请确认包裹里有空位放置道具，否则会要求丢弃一个道具。<br>
<span class="yellow">『基础姿态』</span> 更改自己的行动姿态，各种行动姿态的详情见下文。<br>
<span class="yellow">『应战策略』</span> 更改自己的应战策略，各种应战策略的详情见下文。<br>
<span class="yellow">『留言变更』</span> 修改自己的座右铭，杀人留言和遗言。<br>
<span class="yellow">『包扎伤口』</span>
如果在战斗中受伤，可以使用此功能处理伤口。包扎每处伤口要消耗25点体力，如果体力不足则无法包扎。<br>
<span class="yellow">『武器带电』</span>  给装备着的武器附加“电击”属性，只有特性为“锡安成员”时才具有此功能。需要消耗1个电池/探测器电池
。<br>
<span class="yellow">『查毒』</span> 检查饮食中是否有毒，只有称号为“黑衣组织”时才具有此功能。<br>
<span class="yellow">『武器淬毒』</span> 给装备着的武器附加“带毒”属性，只有称号为“黑衣组织”时才具有此功能。需要消耗1个毒药。</p>


<p><span class="lime">“队伍”</span>：<br>
同一队伍的人可以赠送道具，但是不能互相攻击。注意：在能见度很低的天气状况下可能会误伤。<br>
进入连斗后，所有的队伍都将被取消，并且不能再组建或者加入队伍。<br>
<span class="yellow">『创建队伍』</span> 消耗50点体力，组建一个新的队伍，队伍必须有名称和密码。<br>
<span class="yellow">『加入队伍』</span>
消耗25点体力，加入一个已经存在的队伍，需要填写队伍名称和密码。每支队伍最多有5人。<br>
<span class="yellow">『脱离队伍』</span> 脱离目前已经加入的队伍。</p>
<br>

<p><a name="战斗说明"><span class="subtitle2">6.战斗说明</span></a></p>

<p>战斗分为两种：<span class="yellow">自己引发的战斗</span>和<span class="yellow">其他玩家引发的战斗</span>。<BR>
玩家自己移动或者探索时，遇见了其它的玩家便会发生战斗，这种战斗是由玩家自己引发的。<BR>
玩家有一定的概率先发现对手，此时可以选择<span class="yellow">“攻击”</span>或者<span class="yellow">“逃跑”</span>，还可以向对方<span class="yellow">喊话</span>。<BR>
如果选择攻击，则能先制攻击。此时会依照武器的类型，进行不同类型的攻击。<BR>
如果选择逃走，则一定能回避战斗。<BR>
相反的，如果玩家被对方先发现，就是对方先发动攻击。此时无法回避战斗。<BR>
<BR>
此外，其他玩家也会行动。当一名玩家主动行动而遇到另一名玩家时，主动行动的玩家是战斗的引发者，可以看到详细的作战讯息。<BR>
另一名玩家不属于战斗的引发者，只能看到简要的战斗报告。</p>
<p><span class="lime">下面就是先发现对手时的战斗界面。左边显示自己和对方的部分能力值和状态，右边就是指令区。</span></p>
<p><img  style="border-style: solid;border-width:1px; border-color:#00ff00" src="img/help_2.png" width="947" height="577"></p>
<p>如果选择用武器进行攻击，则会根据自己的攻击力和熟练度，以及对方的防御力，分别计算攻击伤害。<BR>
如果玩家的攻击造成对方的生命值归零，那么对方就被玩家<span class="yellow">杀死</span>了。<BR>
同样的，如果敌人的攻击造成玩家的生命值归零，那么玩家就被敌方杀害，在这局游戏中就无法再进行任何行动了。</p>
<p>引发战斗的玩家，在战斗当中杀害对手时，可以从对方所拥有的武器防具或者金钱中，<span class="yellow">选择一个拿走</span>。<BR>
不过，如果战斗并非由玩家引发，但是在战斗中杀了对方，这种情况下没有办法捡取对方的物品。<BR>
</p>
<br>

<p><a name="熟练度"><span class="subtitle2">7.熟练度</span></a></p>

<p>武器的伤害值跟该武器类型的<span class="yellow">“熟练度”</span>息息相关。<BR>
玩家刚进入游戏时，熟练度低下，可能用武器只能造成1-2点的伤害。<BR>
但是到后期，熟练度升高以后，可能用同样的武器都能将敌人秒杀。<BR>
战斗时，只要<span class="yellow">对敌人进行攻击</span>，无论命中与否，武器类型对应的熟练度都会上升。<BR>
如果逃跑，自然得不到熟练度。<BR>
比较特别的是，<span class="yellow">合成物品</span>和<span class="yellow">设置陷阱</span>也可以增加爆系武器的熟练度。<BR>
阅读<span class="yellow">“技能书”</span>或者使用特定的<span class="yellow">“能力药”</span>也可以提高熟练度。<BR>
此外，使用“装有H173的注射器”进行自杀时，有微小概率会进入<span class="yellow">L5状态</span>，此时全系熟练度都会大幅度上升。
</p>
